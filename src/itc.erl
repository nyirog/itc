-module(itc).

%% API exports
-export([seed/0, norm/1]).

%%====================================================================
%% API functions
%%====================================================================

-type itc() :: {id:id(), event:event()}.

-spec seed() -> itc().
seed() -> {0, 1}.


%%====================================================================
%% Internal functions
%%====================================================================

-spec norm(itc()) -> itc().
norm({Id, Event}) -> {id:norm(Id), event:norm(Event)}.
